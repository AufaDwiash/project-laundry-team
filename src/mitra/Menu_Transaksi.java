    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        String tanggal = sdf.format(jdateTanggal.getDate());
        String batas = sdf.format(jdateBatasWaktu.getDate());
        String member = cmbMember.getSelectedItem().toString();
        String keterangan = txtKeterangan.getText();
        String biaya = txtBiayaTambahan.getText();
        String paket = cmbPaket.getSelectedItem().toString();
        String quantity = txtQuantity.getText();
        String diskon = cmbDiskon.getSelectedItem().toString();

        if (member.isEmpty() || keterangan.isEmpty() || biaya.isEmpty() || paket.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lengkapi semua data terlebih dahulu!");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
        model.insertRow(0, new Object[]{tanggal, member, keterangan, biaya, batas, paket, quantity, diskon});

        JOptionPane.showInternalMessageDialog(this, "Pastikan semua data terisi dengan benar!");


    }//GEN-LAST:event_btnTambahActionPerformed

