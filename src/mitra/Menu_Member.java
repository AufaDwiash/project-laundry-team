    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String id = txtID.getText();
        String nama = txtNama.getText();
        String telepon = txtNoTelp.getText();
        String alamat = txtAlamat.getText();
        String jeniskelamin = cmbJenisKelamin.getSelectedItem().toString();

        // ubah ke singkatan sebelum disimpan
        if (jeniskelamin.equalsIgnoreCase("Laki-Laki")) {
            jeniskelamin = "L";
        } else if (jeniskelamin.equalsIgnoreCase("Perempuan")) {
            jeniskelamin = "P";
        }

        try {
            String sql = "INSERT INTO tb_member (id_member, nama, tlp, jenis_kelamin, alamat) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, id);
            stmt.setString(2, nama);
            stmt.setString(3, telepon);
            stmt.setString(4, jeniskelamin);
            stmt.setString(5, alamat);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");

            loadTableData();  // refresh tabel

            generateAutoID(); // generate ID berikutnya
            btnClearActionPerformed(null); // bersihkan form

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

