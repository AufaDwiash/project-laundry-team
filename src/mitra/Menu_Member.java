    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String id = txtID.getText();
        String nama = txtNama.getText();
        String telepon = txtNoTelp.getText();
        String alamat = txtAlamat.getText();
        String jeniskelamin = cmbJenisKelamin.getSelectedItem().toString();

        // ubah ke singkatan sebelum disimpan
        if (jeniskelamin.equalsIgnoreCase("Laki-Laki")) {
            jeniskelamin = "L";
        } else if (jeniskelamin.equalsIgnoreCase("Perempuan")) {
            jeniskelamin = "P";
        }

        try {
            String sql = "INSERT INTO tb_member (id_member, nama, tlp, jenis_kelamin, alamat) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, id);
            stmt.setString(2, nama);
            stmt.setString(3, telepon);
            stmt.setString(4, jeniskelamin);
            stmt.setString(5, alamat);
            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");

            loadTableData();  // refresh tabel
           
            generateAutoID(); // generate ID berikutnya
            btnClearActionPerformed(null); // bersihkan form

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        try {
            String sql = "UPDATE tb_member SET nama=?, tlp=?, jenis_kelamin=?, alamat=? WHERE id_member=?";
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);

            stmt.setString(1, txtNama.getText());
            stmt.setString(2, txtNoTelp.getText());

            // ðŸ”¹ Konversi Jenis Kelamin ke format 'L' atau 'P'
            String jk = cmbJenisKelamin.getSelectedItem().toString();
            if (jk.equalsIgnoreCase("Laki-Laki")) {
                jk = "L";
            } else if (jk.equalsIgnoreCase("Perempuan")) {
                jk = "P";
            }
            stmt.setString(3, jk);

            stmt.setString(4, txtAlamat.getText());
            stmt.setString(5, txtID.getText());

            stmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
            loadTableData();  // refresh tabel
            btnClearActionPerformed(null); // kosongkan form
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal update data: " + e.getMessage());
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int row = tblMember.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!");
            return;
        }

        String id = txtID.getText();
        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data ID " + id + "?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM tb_member WHERE id_member = ?";
                java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, id);
                stmt.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                loadTableData(); // refresh tabel
                btnClearActionPerformed(null); // kosongkan form
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

