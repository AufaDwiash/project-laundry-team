    private void connection() {
        try {
            String url = "jdbc:mysql://localhost:3306/db_laundry";
            String user = "root";
            String pass = "";
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(url, user, pass);
            System.out.println("Koneksi ke database berhasil!");
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Koneksi database gagal: " + e.getMessage());
        }
    }

    private void loadTableData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("No Telepon");
        model.addColumn("Jenis Kelamin");
        model.addColumn("Alamat");

        try {
            String sql = "SELECT * FROM tb_member ORDER BY id_member ASC";
            java.sql.Statement stmt = conn.createStatement();
            java.sql.ResultSet rs = stmt.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_member"),
                    rs.getString("nama"),
                    rs.getString("tlp"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("alamat")
                });
            }

            tblMember.setModel(model);

            // setelah tabel terisi, generate ID otomatis
            generateAutoID();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

    private void generateAutoID() {
        try {
            String sql = "SELECT id_member FROM tb_member ORDER BY id_member DESC LIMIT 1";
            java.sql.Statement stmt = conn.createStatement();
            java.sql.ResultSet rs = stmt.executeQuery(sql);

            if (rs.next()) {
                // ambil ID terakhir, misal "005"
                String lastID = rs.getString("id_member");
                int nextNum = Integer.parseInt(lastID) + 1;
                String newID = String.format("%03d", nextNum); // hasil: 006
                txtID.setText(newID);
            } else {
                txtID.setText("001"); // kalau belum ada data
            }

            txtID.setEditable(false); // user tetap tidak bisa ubah

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal generate ID otomatis: " + e.getMessage());
        }
    }

