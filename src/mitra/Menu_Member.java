    private void tblMemberMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMemberMouseClicked
        // TODO add your handling code here:
        int baris = tblMember.rowAtPoint(evt.getPoint());

        // Ambil data dari tabel sesuai kolom
        String id = tblMember.getValueAt(baris, 0).toString();
        String nama = tblMember.getValueAt(baris, 1).toString();
        String telp = tblMember.getValueAt(baris, 2).toString();
        String jk = tblMember.getValueAt(baris, 3).toString();
        String alamat = tblMember.getValueAt(baris, 4).toString();

        // Set ke form
        txtID.setText(id);
        txtNama.setText(nama);
        txtNoTelp.setText(telp);
        txtAlamat.setText(alamat);

        // Konversi enum ke teks combobox
        if (jk.equalsIgnoreCase("L")) {
            cmbJenisKelamin.setSelectedItem("Laki-Laki");
        } else if (jk.equalsIgnoreCase("P")) {
            cmbJenisKelamin.setSelectedItem("Perempuan");
        }
    }//GEN-LAST:event_tblMemberMouseClicked

