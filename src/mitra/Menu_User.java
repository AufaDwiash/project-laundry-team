    private void btnUbahPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahPasswordActionPerformed

        String usernameLama = txtUsernameLama.getText().trim();
        String passwordLama = txtPasswordLama.getText().trim();
        String usernameBaru = txtUsernameBaru.getText().trim();
        String passwordBaru = txtPasswordBaru.getText().trim();

        // Validasi input lama tidak boleh kosong
        if (usernameLama.isEmpty() || passwordLama.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Username lama dan Password lama wajib diisi.",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ðŸ” CEK KEAMANAN: cocok tidak dengan data di database?
        if (!isOldCredentialValid(usernameLama, passwordLama)) {
            JOptionPane.showMessageDialog(this,
                    "Username atau Password lama salah.\nSilakan periksa kembali.",
                    "Keamanan",
                    JOptionPane.ERROR_MESSAGE);

            // opsional: kosongkan password lama supaya user ketik ulang
            txtPasswordLama.setText("");
            return; // STOP, jangan lanjut update
        }

        // Kalau sudah lolos cek keamanan, baru lanjut ke update
        if (usernameBaru.isEmpty() && passwordBaru.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Isi minimal salah satu: Username baru atau Password baru.",
                    "Peringatan",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Kalau usernameBaru kosong â†’ pakai usernameLama
        if (usernameBaru.isEmpty()) {
            usernameBaru = usernameLama;
        }

        // Kalau passwordBaru kosong â†’ pakai passwordLama
        if (passwordBaru.isEmpty()) {
            passwordBaru = passwordLama;
        }

        String sqlUpdate = "UPDATE tb_user SET username = ?, password = ? WHERE username = ?";
        // GANTI 'tb_user', 'username', 'password' sesuai database kamu

        try (PreparedStatement ps = conn.prepareStatement(sqlUpdate)) {
            ps.setString(1, usernameBaru);
            ps.setString(2, passwordBaru);
            ps.setString(3, usernameLama);

            int affected = ps.executeUpdate();
            if (affected > 0) {
                JOptionPane.showMessageDialog(this,
                        "Akun berhasil diperbarui.",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);

                // update tampilan lama
                txtUsernameLama.setText(usernameBaru);
                txtPasswordLama.setText(passwordBaru);

                // kosongkan field baru
                txtUsernameBaru.setText("");
                txtPasswordBaru.setText("");
            } else {
                JOptionPane.showMessageDialog(this,
                        "Gagal memperbarui akun. Data tidak ditemukan.",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan saat update akun: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahPasswordActionPerformed

