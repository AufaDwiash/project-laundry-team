    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);

        String query = "SELECT * FROM tb_user";

        try (Statement stmt = conn.createStatement(); ResultSet rs = stmt.executeQuery(query)) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_user"),
                    rs.getString("nama"),
                    rs.getString("username"),
                    rs.getString("password"),
                    rs.getString("role"),});
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

    private void connect() {
        try {
            String url = "jdbc:mysql://localhost:3306/db_laundry";
            String user = "root";
            String pass = "";
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(url, user, pass);
            System.out.println("Koneksi database berhasil!");
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Koneksi database gagal: " + e.getMessage());
        }
    }

    private void autoID() {
        try {
            String sql = "SELECT id_user FROM tb_user ORDER BY id_user DESC LIMIT 1";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String lastID = rs.getString("id_user");
                int num = Integer.parseInt(lastID.replaceAll("\\D+", "")) + 1;
                txtID.setText(String.format("%03d", num)); // contoh: USR001, USR002
            } else {
                txtID.setText("001");
            }
            txtID.setEditable(false);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal membuat ID otomatis: " + e.getMessage());
        }
    }

