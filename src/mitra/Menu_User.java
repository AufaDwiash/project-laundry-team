    private void connect() {
        try {
            String url = "jdbc:mysql://localhost:3306/db_laundry";
            String user = "root";
            String pass = "";
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(url, user, pass);
            System.out.println("Koneksi database berhasil!");
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Koneksi database gagal: " + e.getMessage());
        }
    }

    private void loadUserData() {
        String sql;
        boolean useParam = currentUsername != null && !currentUsername.isEmpty();

        if (useParam) {
            // kalau currentUsername sudah ada, pakai itu
            sql = "SELECT username, password FROM tb_user WHERE username = ?";
        } else {
            // kalau belum ada, ambil user pertama dari tabel
            sql = "SELECT username, password FROM tb_user LIMIT 1";
        }

        try (PreparedStatement ps = conn.prepareStatement(sql)) {

            if (useParam) {
                ps.setString(1, currentUsername);
            }

            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    // update currentUsername kalau tadi belum ada
                    currentUsername = rs.getString("username");

                    txtUsernameLama.setText(currentUsername);
                    txtPasswordLama.setText(rs.getString("password"));

                    // form lama bisa diubah (enable)
                    txtUsernameLama.setEditable(true);
                    txtPasswordLama.setEditable(true);

                } else {
                    JOptionPane.showMessageDialog(this,
                            "Data user tidak ditemukan di tabel tb_user.",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal load data user: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean isOldCredentialValid(String usernameLama, String passwordLama) {
        String sql = "SELECT * FROM tb_user WHERE username = ? AND password = ?";
        // GANTI 'tb_user', 'username', 'password' sesuai tabel kamu

        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, usernameLama);
            ps.setString(2, passwordLama);

            try (ResultSet rs = ps.executeQuery()) {
                return rs.next(); // true kalau ditemukan
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan saat cek akun lama: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

